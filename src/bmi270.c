typedef struct {
    FT_HANDLE handle;
    int16_t accel_lsb_per_g;
    float gyro_lsb_per_dps;
    int16_t accel_sample_count;
    int16_t gyro_sample_count;
    uint8_t rx_bytes[2048];
    AccelUnits converted_accel[MAX_FIFO_SAMPLES_PER_SENSOR_PER_READ];
    GyroUnits converted_gyro[MAX_FIFO_SAMPLES_PER_SENSOR_PER_READ];
} MotionStatus;

MotionStatus current;

void spi_shift_bytes_with_cs(const uint8_t *tx, uint32_t tx_len, uint8_t *rx, uint32_t rx_len)
{
    // For some reason, SPI_ReadWrite() does not shift any data out, might be
    // an issue with the mingw version of the libmpsse.a file packaged with v0.6
    uint32_t options;
    uint32_t bytes_transferred;
    FT_STATUS status;
    options = SPI_TRANSFER_OPTIONS_SIZE_IN_BYTES | SPI_TRANSFER_OPTIONS_CHIPSELECT_ENABLE;
    status = SPI_Write(current.handle, (uint8_t *)tx, tx_len, &bytes_transferred, options);
    if (status != FT_OK)
        LOG;
    if (bytes_transferred != tx_len)
        LOG;
    options |= SPI_TRANSFER_OPTIONS_CHIPSELECT_DISABLE;
    status = SPI_Read(current.handle, rx, rx_len, &bytes_transferred, options);
    if (status != FT_OK)
        LOG;
    if (bytes_transferred != rx_len)
        LOG;
}

void spi_write_with_cs(const uint8_t *tx, uint32_t tx_len)
{
    uint32_t options;
    uint32_t bytes_transferred;
    FT_STATUS status;
    options = SPI_WRITE_OPTIONS;
    status = SPI_Write(current.handle, (uint8_t *)tx, tx_len, &bytes_transferred, options);
    if (status != FT_OK)
        LOG;
    if (bytes_transferred != tx_len)
        LOG;
}

void print_list_in_lines(uint8_t *arr, uint32_t size)
{
    uint32_t i;
    for (i = 0; i < size; i++) {
        printf("[%ld]: 0x%02x ", i, arr[i]);
        if ((!(i % 8)) && i) {
            // After every 8th entry, move down a line
            printf("\n");
        }
    }
    printf("\n");
}

void convert_temp_raw_to_c(int16_t raw_out, float *temp_c)
{
    *temp_c = ((float)raw_out / TEMP_SENSITIVITY_LSB_PER_K) + (LSB_0_TEMP_C);
}

void convert_gyro_raw_to_dps(int16_t raw_out, float lsb_per_dps, float *dps)
{
    *dps = (float)(raw_out / lsb_per_dps);
}

void convert_accel_raw_to_g(int16_t raw_out, int16_t lsb_per_g, float *g)
{
    *g = ((float)raw_out / lsb_per_g);
}


void convert_gyro_dps_to_rad_per_sec(float dps, float *rad_per_sec)
{
    *rad_per_sec = dps * DEG_TO_RAD;
}

void convert_accel_g_to_m_per_s2(float g, float *m_per_s2)
{
    *m_per_s2 = g * G_TO_M_PER_S_SQUARED;
}

